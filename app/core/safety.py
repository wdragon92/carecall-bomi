def _matched(text: str, phrase: str) -> bool:
    idx = text.find(phrase)
    while idx != -1:
        tail = text[idx + len(phrase): idx + len(phrase) + 7]
        if not any(n in tail for n in _NEG):
            return True
        idx = text.find(phrase, idx + 1)
    return False


def scan(text: str) -> list[dict]:
    """어르신 발화에서 위험신호 탐지. content별 최초 매칭 1건씩 반환.
    반환 dict는 Finding alias(한글 키) + 내부용 '_kind'."""
    text = text or ""
    seen: set[str] = set()
    out: list[dict] = []
    for r in RULES:
        if r.content in seen:
            continue
        if any(_matched(text, p) for p in r.phrases):
            seen.add(r.content)
            out.append({
                "카테고리": r.category,
                "내용": r.content,
                "심각도": r.severity,
                "사람_개입_필요": r.needs_human,
                "_kind": r.kind,
            })
    return out


def alert(kinds: set[str], llm_flags: set[str] | frozenset[str] = frozenset()) -> tuple[str | None, str | None]:
    """탐지된 kind + LLM 심각신호(flags ⊆ {"medical","psych","fraud"})로 경보 레벨·문구 결정.
    연계 원칙: 자살예방상담 109는 심리 신호에만, 건강 응급은 119·보호자, 사기는 112·1332로 분리.
    수동적(완곡) 자살신호는 warning 고정(직접 표현만 emergency).
    mood_low(우울·무기력)는 배너 없이 카드로만."""
    # 결정적(코드) 탐지를 LLM 플래그보다 먼저 — LLM이 의료 응급을 '긴급(심리)'로 잘못 라벨해도
    # 코드가 잡은 의료 응급이 109(심리 전용) 문구에 가려지지 않게 한다.
    if "suicide_acute" in kinds:
        return "emergency", "위급한 신호가 감지됐어요. 지금 자살예방상담 109 또는 119, 곁의 보호자·담당자에게 바로 연결하시길 권해요."
    if "medical_emergency" in kinds:
        return "emergency", "응급이 의심되는 몸 신호가 있어요. 지금 바로 119에 연락하시고, 보호자께도 알려주세요."
    if "medical" in llm_flags:
        return "emergency", "응급이 의심되는 몸 신호가 있어요. 지금 바로 119에 연락하시고, 보호자께도 알려주세요."
    if "suicide_warning" in kinds:
        return "warning", "마음이 많이 지치신 것 같아 걱정돼요. 혼자 힘들어하지 마시고, 24시간 자살예방상담 109나 가까운 분과 이야기 나눠보시길 권해요."
    if "psych" in llm_flags:
        # LLM 단독 심리 플래그는 warning — 급성 신호(죽고 싶다 등 직접 표현)는 결정망이
        # 전담하고, LLM 환각(무해한 거절을 '긴급'으로)이 자살 배너를 오발하면
        # 진짜 경보의 신뢰가 깎이는 실측 대응(오경보 피로)
        return "warning", "마음이 많이 힘드신 것 같아 걱정돼요. 혼자 견디지 마시고, 24시간 자살예방상담 109나 가까운 분과 이야기 나눠보시길 권해요."
    if "fraud_exposure" in kinds or "fraud" in llm_flags:
        return "warning", "사기가 의심되는 정황이 있어요. 돈·개인정보는 절대 보내지 마시고, 이미 보내셨다면 지금 바로 경찰 112와 은행 지급정지, 금융감독원 1332에 연락하세요."
    if "medical_soon" in kinds:
        return "warning", "몸에 신경 쓰이는 신호가 있어요. 그냥 두지 마시고 가까운 시일 안에 진료를 받아보시고, 보호자께도 알려드리세요."
    return None, None
