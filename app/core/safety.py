def _negated(text: str, start: int, end: int) -> bool:
    """구절(text[start:end])이 '직접' 부정되는지 판정. 무관한 뒷절의 부정어에 반응하지 않는다."""
    # 구절 바로 앞 단형 부정: "안 ~", "못 ~"
    if text[max(0, start - 2):start] in ("안 ", "못 "):
        return True
    tail = text[end:end + 16]
    # 구절 술어에 바로 붙는 장형 부정: "~지 않", "~건 아니" 등
    if any(n in tail for n in _NEG_TAIL):
        return True
    # 명사화 '존재 부정'만: 의미명사(생각/마음/뜻) + 존재부정(없/아니/안 해). '말도 못 하고'류
    # (표현명사 + 못 하다)는 실제 SI 고백일 수 있어 탐지 유지.
    if any(n in tail for n in _NEG_NOUN) and any(g in tail for g in _NEG_NOUN_NEG):
        return True
    return False


def _matched(text: str, phrase: str) -> bool:
    idx = text.find(phrase)
    while idx != -1:
        if not _negated(text, idx, idx + len(phrase)):
            return True
        idx = text.find(phrase, idx + 1)
    return False


def scan(text: str) -> list[dict]:
    """어르신 발화에서 위험신호 탐지. content별 최초 매칭 1건씩 반환.
    반환 dict는 Finding alias(한글 키) + 내부용 '_kind'."""
    text = text or ""
    seen: set[str] = set()
    out: list[dict] = []
    for r in RULES:
        if r.content in seen:
            continue
        if any(_matched(text, p) for p in r.phrases):
            seen.add(r.content)
            out.append({
                "카테고리": r.category,
                "내용": r.content,
                "심각도": r.severity,
                "사람_개입_필요": r.needs_human,
                "_kind": r.kind,
            })
    return out


